    public function run(): void
    {
        $pueblosPorNombre = Pueblo::pluck('id', 'nombre');
        $categoriasPorNombre = Categoria::deGrupo('punto_interes')->pluck('id', 'nombre');
        $slugsUsados = PuntoInteres::pluck('slug')->flip()->all();

        $importados = 0;
        $omitidos = 0;

        foreach (self::PUNTOS as $datos) {
            $puebloId = $pueblosPorNombre[$datos['pueblo']] ?? null;

            if (! $puebloId) {
                $this->command?->warn("Pueblo no encontrado: {$datos['pueblo']}");
                $omitidos++;

                continue;
            }

            $slug = $this->slugUnico($datos['nombre'], $puebloId, $slugsUsados);

            $punto = PuntoInteres::updateOrCreate(
                ['pueblo_id' => $puebloId, 'nombre' => $datos['nombre']],
                [
                    'slug' => $slug,
                    'descripcion' => $datos['descripcion'],
                    'direccion' => $datos['direccion'],
                    'foto' => $datos['foto'],
                    'latitud' => $datos['latitud'],
                    'longitud' => $datos['longitud'],
                ]
            );

            if ($datos['categorias']) {
                $ids = $categoriasPorNombre->only($datos['categorias'])->values();

                if ($ids->isNotEmpty()) {
                    $punto->categorias()->syncWithoutDetaching($ids);
                }
            }

            $slugsUsados[$slug] = true;
            $importados++;
        }

        $this->command?->info("Puntos de interés históricos importados: {$importados} (omitidos: {$omitidos}).");
    }

    /**
     * @param  array<string, bool>  $slugsUsados
     */
    private function slugUnico(string $nombre, int $puebloId, array &$slugsUsados): string
    {
        $base = Str::slug($nombre);
        $slug = $base;

        if (isset($slugsUsados[$slug])) {
            $slug = $base.'-'.$puebloId;
        }

        $sufijo = 2;
        while (isset($slugsUsados[$slug])) {
            $slug = $base.'-'.$puebloId.'-'.$sufijo;
            $sufijo++;
        }

        return $slug;
    }
